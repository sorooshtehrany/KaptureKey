//! \brief  Configure buzzer gpio and pwm timer
//******************************************************************************
void buzzerInit( void )
{   
    //! init pwm pulse ---------------------------------------------------------
    //! buzzer gpio init
    GPIO_Init( BUZZER_PORT, BUZZER_PIN, BUZZER_MODE ); 
    //! buzzer power enable
    GPIO_Init( GPIOA, GPIO_Pin_4, GPIO_Mode_Out_PP_High_Fast );
    //! test
    //GPIO_Init( GPIOD, GPIO_Pin_6, GPIO_Mode_Out_PP_High_Fast );
    
    //! buzzer timer pwm init
    //! Enable TIM1 clock 
    CLK_PeripheralClockConfig( CLK_Peripheral_TIM1, ENABLE );
    TIM1_DeInit();
    //! Time base configuration ( 1Khz )
    TIM1_TimeBaseInit( 8, TIM1_CounterMode_Up, 255, 0 );
    
    TIM1_OC2Init( TIM1_OCMode_PWM1, TIM1_OutputState_Enable, TIM1_OutputNState_Disable,
                  0, TIM1_OCPolarity_Low, TIM1_OCNPolarity_Low, TIM1_OCIdleState_Reset,
                  TIM1_OCNIdleState_Reset );
    
    TIM1_OC2PreloadConfig(ENABLE);

    TIM1_ARRPreloadConfig(ENABLE);

    /* Enable TIM1 outputs */
    TIM1_CtrlPWMOutputs(DISABLE);
    /* TIM1 enable counter */
    TIM1_Cmd(ENABLE);
    //! ------------------------------------------------------------------------
    
    //! timer interrupt --------------------------------------------------------
    //! Enable TIM4 clock 
    CLK_PeripheralClockConfig( CLK_Peripheral_TIM2, ENABLE );
    
    //! Time base configuration 
    TIM2_TimeBaseInit( TIM2_Prescaler_2, TIM2_CounterMode_Up, 980 );
    //TIM2_TimeBaseInit( TIM2_Prescaler_8, TIM2_CounterMode_Up, 175 );
    
    //! Clear TIM4 update flag 
    TIM2_ClearFlag(TIM2_FLAG_Update);
    
    //! Enable update interrupt 
    TIM2_ITConfig(TIM2_IT_Update, ENABLE);
    
    //! enable interrupts 
    //enableInterrupts();

    //! Enable TIM4 
    TIM2_Cmd(ENABLE);
    //! ------------------------------------------------------------------------
}
//******************************************************************************
//! \brief  buzzer beep
//******************************************************************************
void buzzerBeep     ( void )
{
    if( buzzerBeepCounter>0 )
    {
      
        TIM1_CtrlPWMOutputs(ENABLE);
        vibrationSet();
        
        GPIO_SetBits(GPIOA, GPIO_Pin_4);
        TIM1_OC2Init( TIM1_OCMode_Toggle, TIM1_OutputState_Enable, TIM1_OutputNState_Disable,
                      buzzerAudioError[buzzerTimeCounter], TIM1_OCPolarity_Low, TIM1_OCNPolarity_Low, 
                      TIM1_OCIdleState_Set, TIM1_OCNIdleState_Set );
        
        buzzerTimeCounter++;
        if( buzzerTimeCounter>8000 )
        {
            buzzerTimeCounter=0;
            buzzerBeepCounter--;
            if( buzzerBeepCounter==0 )
            {
              
                TIM1_CtrlPWMOutputs(DISABLE);
                vibrationReset();
                GPIO_ResetBits(GPIOA, GPIO_Pin_4);
                TIM1_OC2Init( TIM1_OCMode_Toggle, TIM1_OutputState_Enable, TIM1_OutputNState_Disable,
                      0, TIM1_OCPolarity_Low, TIM1_OCNPolarity_Low, 
                      TIM1_OCIdleState_Set, TIM1_OCNIdleState_Set );   
            }
        }
    }
}
